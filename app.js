  document.getElementById('apply').addEventListener('click',()=>{
    const element = document.querySelector('input[name="filterOption"]:checked')
    const ans = element.value;

    if(ans==="rating"){
      res.sort((a,b)=>b.rating-a.rating)
    }
    else if(ans==="highLow"){
      res.sort((a,b)=>b.Price_for_two-a.Price_for_two)
    }
    else if(ans==="costLowHigh"){
      res.sort((a,b)=>a.Price_for_two-b.Price_for_two)
    }
    else if(ans==="distance"){
      res.sort((a,b)=>a.Distance_from_Customer_house-b.Distance_from_Customer_house)
    }

    document.getElementById('root').innerHTML=""
    getrestaurants(res)
  })

